def print_seq():

    print('Sequenza a):')
    for a in range(2,15,2):

        print(a)

    print()

    print('Sequenza b):')
    for b in range(1,14,3):

        print(b)

    print()

    print('Sequenza c):')
    c: int = 30
    while(c >= 0):

        print(c)

        c -= 5

    print('Sequenza d):')
    for d in range(5,46,10):

        print(d)
    

print_seq()

	

